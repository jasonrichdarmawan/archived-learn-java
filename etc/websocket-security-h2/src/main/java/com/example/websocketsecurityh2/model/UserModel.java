package com.example.websocketsecurityh2.model;

public class UserModel {
  private String id;
  private String user;
  private String password;
  private boolean active;

  public String getUser() {
    return user;
  }

  public String getPassword() {
    return password;
  }

  public boolean getActive() {
    return active;
  }

  public String getId() {
    return id;
  }
}
